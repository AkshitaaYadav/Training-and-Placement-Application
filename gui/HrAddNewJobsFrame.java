    private void loadData(){
        try{
            int id = JobDAO.getNewJobId();
            lblJobId.setText("Job-" + (id));
            if(txtCompanyName.getText().trim().isEmpty()){
                txtCompanyName.setText(HrDAO.getCompanyName(CurrentUser.getId()));
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error in Generating HR ID...", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
    
    private boolean validateInputs(){
        if(txtJobTitle.getText().trim().isEmpty() || txtSkill1.getText().trim().isEmpty() || txtSkill2.getText().trim().isEmpty() || txtSkill3.getText().trim().isEmpty() || txtSkill4.getText().trim().isEmpty()){
            return false;
        }
        return true;
    }
    
