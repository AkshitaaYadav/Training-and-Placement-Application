    private void btnAddJobActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddJobActionPerformed
        if(!validateInputs()){
            JOptionPane.showMessageDialog(null, "All Fields are required", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        JobPOJO job = new JobPOJO();
        job.setJobId(lblJobId.getText().trim());
        job.setJobTitle(txtJobTitle.getText().trim());
        job.setHrId(CurrentUser.getId());
        String tags = txtSkill1.getText().trim() + ", " + txtSkill2.getText().trim() + ", " + txtSkill3.getText().trim() + ", " + txtSkill4.getText().trim();
        job.setTags(tags);
        job.setStatus(0);
        
        try{
            boolean ans = JobDAO.addNewJob(job);
            if(ans){
                JOptionPane.showMessageDialog(null, "Job Added Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                clearFields();
                loadData();
                return;
            }
            JOptionPane.showMessageDialog(null, "Sorry! could not Add the Job", "Error", JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Facing Some Technical Issue", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnAddJobActionPerformed

    private void clearFields(){
        txtJobTitle.setText("");
        txtSkill1.setText("");
        txtSkill2.setText("");
        txtSkill3.setText("");
        txtSkill4.setText("");
    }
    
