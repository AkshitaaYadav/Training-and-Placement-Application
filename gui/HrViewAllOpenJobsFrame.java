     private void loadAllJobs(){
        try{
            allOpenJobs = JobDAO.getAllAvailableJobsForHr(CurrentUser.getId());
            for(ParticipantJobPOJO job : allOpenJobs){
            Vector <String> row = new Vector<>();
            row.add(job.getJobId());
            row.add(job.getJobTitle());
            row.add(job.getCompanyName());
            row.add(job.getTags());
            model.addRow(row);    
            }
            if(allOpenJobs.isEmpty()){
//            JOptionPane.showMessageDialog(rootPane, this);
            }
        } 
        
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "DB-Error while loading Jobs", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
     }
    
