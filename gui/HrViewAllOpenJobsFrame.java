    private void btnApplyForJobActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyForJobActionPerformed
        int row = tblAllOpenJobs.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(null, "Please select a job first", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else{
            ResultPOJO pojo = new ResultPOJO();
            pojo.setPid(CurrentUser.getId());
            pojo.setJobid(tblAllOpenJobs.getValueAt(row, 0).toString().trim());
            pojo.setPercentage(-1.0);
            pojo.setSelectedByHr("no");
            
            try{
                boolean result = ResultDAO.applyForJob(pojo);
                if(result){
                    String name = "Your Name : " + CurrentUser.getName();
                    String pid = "Your Participant Id : " + CurrentUser.getId();
                    String jobtitle = "You Applied For The Job Title : " + tblAllOpenJobs.getValueAt(row, 1) + " Of " + tblAllOpenJobs.getValueAt(row, 2);
                    DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/yyyy");
                    LocalDateTime today = LocalDateTime.now();
                    String details;
                    details = name + "\n" + pid + "\n" + jobtitle + "\n" + "Date : " + dtf.format(today);
                    JOptionPane.showMessageDialog(null, "You Successfully Applied For " + tblAllOpenJobs.getValueAt(row, 0) + "\t\t\n\n" + details, "Success", JOptionPane.INFORMATION_MESSAGE);
                }
                else{
                    JOptionPane.showMessageDialog(null, "You Hava Already Applied For This Job", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(null, "DB Error while applying for the job", "Error", JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnApplyForJobActionPerformed

