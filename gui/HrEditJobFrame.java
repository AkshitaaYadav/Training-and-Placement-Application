    private void btnEditJobActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditJobActionPerformed
        if(!validateInputs()){
            JOptionPane.showMessageDialog(null, "All Fields are required", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        job.setJobTitle(txtJobTitle.getText().trim());
        String tags = txtSkill1.getText().trim() + ", " + txtSkill2.getText().trim() + ", " + txtSkill3.getText().trim() + ", " + txtSkill4.getText().trim();
        job.setTags(tags);
        try{
            boolean result = JobDAO.editJob(job);   
            if(result){
                JOptionPane.showMessageDialog(null, "Job Editted Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                clearFields();
                new HrChooseJobForEdittingFrame().setVisible(true);
                return;
            }
            JOptionPane.showMessageDialog(null, "Sorry! could not Edit the Job", "Error", JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Facing Some Technical Issue", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnEditJobActionPerformed

