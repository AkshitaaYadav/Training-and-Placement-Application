    private void showJobDetails(){
        String companyName = "";
        try{
            System.out.println(job.getHrId());
            companyName = HrDAO.getCompanyName(job.getHrId());
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "DB Error", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        txtCompanyName.setText(companyName);
        txtJobTitle.setText(job.getJobTitle());
        lblJobId.setText(job.getJobId());
        String alljobs[] = job.getTags().split(",");
        txtSkill1.setText(alljobs[0]);
        txtSkill2.setText(alljobs[1]);
        txtSkill3.setText(alljobs[2]);
        txtSkill4.setText(alljobs[3]);
    }
    
    private boolean validateInputs(){
        if(txtJobTitle.getText().trim().isEmpty() || txtSkill1.getText().trim().isEmpty() || txtSkill2.getText().trim().isEmpty() || txtSkill3.getText().trim().isEmpty() || txtSkill4.getText().trim().isEmpty()){
            return false;
        }
        return true;
    }
    
